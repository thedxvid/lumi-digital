export function getLumiResponse(userMessage: string, conversationHistory: Message[] = []): string {
  const message = userMessage.toLowerCase();
  
  // Detect module based on keywords
  if (message.includes('começar') || message.includes('iniciante') || message.includes('primeiro') || message.includes('zero')) {
    return getRandomResponse('iniciantes');
  }
  
  if (message.includes('ferramenta') || message.includes('como usar') || message.includes('tutorial') || message.includes('configurar')) {
    return getRandomResponse('ferramentas');
  }
  
  if (message.includes('ganhar') || message.includes('renda') || message.includes('dinheiro') || message.includes('monetizar')) {
    return getRandomResponse('renda');
  }
  
  if (message.includes('copy') || message.includes('script') || message.includes('texto') || message.includes('venda')) {
    return getRandomResponse('copys');
  }
  
  if (message.includes('motivação') || message.includes('medo') || message.includes('desistir') || message.includes('mental')) {
    return getRandomResponse('mental');
  }
  
  // Default response for general questions
  return getRandomResponse('general');
}

function getRandomResponse(category: string): string {
  const responses = responseBank[category] || responseBank.general;
  return responses[Math.floor(Math.random() * responses.length)];
}

export function generateConversationTitle(firstMessage: string): string {
  const message = firstMessage.toLowerCase();
  
  if (message.includes('começar') || message.includes('iniciante')) {
    return 'Primeiros passos no digital';
  }
  if (message.includes('ferramenta')) {
    return 'Ferramentas descomplicadas';
  }
  if (message.includes('ganhar') || message.includes('renda')) {
    return 'Estratégias de renda';
  }
  if (message.includes('copy') || message.includes('script')) {
    return 'Copys e scripts';
  }
  if (message.includes('motivação') || message.includes('mental')) {
    return 'Suporte mental';
  }
  
  return 'Conversa com LUMI';
}